package Inmueble;

public class CasaPueblo {
private double supplantas;
private double suppatio;
private int numplantas;

public CasaPueblo(double supplantas, double suppatio, int numplantas) {
	this.supplantas = supplantas;
	this.suppatio = suppatio;
	this.numplantas = numplantas;
}

public double getSupplantas() {
	return supplantas;
}

public void setSupplantas(double supplantas) {
	this.supplantas = supplantas;
}

public double getSuppatio() {
	return suppatio;
}

public void setSuppatio(double suppatio) {
	this.suppatio = suppatio;
}

public int getNumplantas() {
	return numplantas;
}

public void setNumplantas(int numplantas) {
	this.numplantas = numplantas;
}



}
